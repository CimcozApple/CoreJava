//Using a Enum
public enum PaymentType {
	//Use () to call the constructor
	//It is a good practice to represent final variables in all uppercase
	DEBITCARD(5), CREDITCARD(0), CASH(10);
	//DEFINING AND USING FIELDS
	int fee;
	
	PaymentType(int fee){
		this.fee = fee;
	}
	
	public int getFee(){
		return this.fee;
	}
	public void setFee(int fee){
		this.fee = fee;
	}
	/*
	 * Which method can be used to find out the position of a constant in a Enum? Ordinal
	 * Can an Enum extend another class? No
	 * The values in an Enum are: public, static, final
	 */
}
